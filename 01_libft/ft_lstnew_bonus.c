#include "libft.h"

t_list	*ft_lstnew(void *content)
{
	t_list	*new;

	new = (t_list *)malloc(sizeof(t_list));
	if (!new)
		return ((t_list *)(void *)0);
	new -> content = content;
	new -> next = (t_list *)(void *) 0;
	return (new);
}
