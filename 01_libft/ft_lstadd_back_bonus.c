#include "libft.h"

void	ft_lstadd_back(t_list **lst, t_list *new)
{
	t_list	*ptr;

	ptr = *lst;
	if (!ptr)
	{
		*lst = new;
		return ;
	}
	while (ptr && ptr -> next)
		ptr = ptr -> next;
	if (ptr)
		ptr -> next = new;
	else
		*lst = new;
}
